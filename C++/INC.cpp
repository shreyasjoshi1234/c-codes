#include<iostream>
using namespace std;

//
int main()
{
    int n=4;
    cout<<" "<<++n<<endl;
    cout<<" "<<n<<endl;
    cout<<" "<<n++<<endl;
    cout<<" "<<n<<endl;
    cout<<" "<<+n<<endl;
    cout<<" "<<-n<<endl;
    cout<<" "<<n<<endl;
    cout<<" "<<--n<<endl;
    cout<<" "<<n<<endl;
    cout<<" "<<n--<<endl;
    cout<<" "<<n<<endl;
}